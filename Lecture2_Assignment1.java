//1. Try to declare meaningful variables of each type.
// Eg - a variable named age should be a numeric type (int or float) not byte.
public class Lecture2_Assignment1 {
    public static void main(String[] args) {
        byte val = 123;  // -128 to 127
        short val1 = 12; // -32,768 to 32,767
        int age = 45;    // -2,147,483,648 to 2,147,483,647
        long num = 123456789l; // -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        float pi = 3.14f; // 6 to 7 decimal digits
        double dub = 22.123458384; // 15 decimal digits
        char ch = 'a'; // Stores a single character/letter or ASCII values
        boolean bool = true; // Stores true or false values
    }
}
